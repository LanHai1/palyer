  transition: all 0.3s ease;
}
.slide-leave-active {
  transition: all 3s cubic-bezier(1, 0.5, 0.8, 1);
}
.slide-enter,
.slide-leave-to {
  transform: translateY(50px);
  /* opacity: 0; */
}
